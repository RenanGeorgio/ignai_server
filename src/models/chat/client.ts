import mongoose from "../../database";

const { Schema } = mongoose;

const clientSchema = new Schema({
    name: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: Date.now,
    },
});

const ClientModel = mongoose.model("Client", clientSchema);

export default ClientModel;